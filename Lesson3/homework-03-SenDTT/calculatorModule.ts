export const add = (n1: number, n2: number): number => {
    return n1 + n2;
}

export const substract = (n1: number, n2: number): number => {
    return n1 - n2;
}

export const multiply = (n1: number, n2: number): number => {
    return n1 * n2;
}

export const divide = (n1: number, n2: number): number => {
    return n1 / n2;
}